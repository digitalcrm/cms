<?php

namespace App\Mail;

use Illuminate\Bus\Queueable;
use Illuminate\Contracts\Queue\ShouldQueue;
use Illuminate\Mail\Mailable;
use Illuminate\Queue\SerializesModels;

class ConfirmedNewsletter extends Mailable
{
    use Queueable, SerializesModels;

    public $userData;

    /**
     * Create a new message instance.
     *
     * @return void
     */
    public function __construct($userData)
    {
        $this->userData = $userData;
    }

    /**
     * Build the message.
     *
     * @return $this
     */
    public function build()
    {
        $subscriptionURL = route('confirmed.subscription',['token' => $this->userData->token]);

        return $this->subject('Confirm Your Subscription')
                    ->markdown('emails.newsletter.confirmed-newsletter')
                    ->with('subscriptionURL', $subscriptionURL);
    }
}
